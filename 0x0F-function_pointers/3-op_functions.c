#include "3-calc.h"

/**
 * op_add - calculates sum
 * @a: int
 * @b: another int
 * Return: sum of two ints
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub _ calculates difference
 * @a: int
 * @b: another int
 * Return: difference of two ints
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calcs product
 * @a: int
 * @b: another int
 * Return: product of two ints
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calcs division
 * @a: int
 * @b: another int
 * Return: div of two ints
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calc remainder
 * @a: int
 * @b: another int
 * Return: remainder of an int
 */

int op_mod(int a, int b)
{
	return (a % b);
}
